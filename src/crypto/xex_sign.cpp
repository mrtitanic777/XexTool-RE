// ---- minimal fixed-width big integer (2112-bit, uint32 limbs LE) ----------
constexpr int NL = 66;
typedef std::array<uint32_t, NL> Big;

int big_cmp(const Big& a, const Big& b) {
    for (int i = NL - 1; i >= 0; --i)
        if (a[i] != b[i]) return a[i] < b[i] ? -1 : 1;
    return 0;
}
void big_sub(Big& a, const Big& b) {   // a -= b (a >= b)
    int64_t borrow = 0;
    for (int i = 0; i < NL; ++i) {
        int64_t t = (int64_t)a[i] - b[i] - borrow;
        if (t < 0) { t += 0x100000000LL; borrow = 1; } else borrow = 0;
        a[i] = (uint32_t)t;
    }
}
void big_add(Big& a, const Big& b) {   // a += b
    uint64_t carry = 0;
    for (int i = 0; i < NL; ++i) {
        uint64_t t = (uint64_t)a[i] + b[i] + carry;
        a[i] = (uint32_t)t; carry = t >> 32;
    }
}
void big_shl1(Big& a) {                 // a <<= 1
    uint32_t carry = 0;
    for (int i = 0; i < NL; ++i) {
        uint32_t nc = a[i] >> 31;
        a[i] = (a[i] << 1) | carry; carry = nc;
    }
}
inline bool big_bit(const Big& a, int k) { return (a[k >> 5] >> (k & 31)) & 1u; }

Big big_from_be(const uint8_t* b) {     // 256 big-endian bytes -> 2048-bit
    Big x{};
    for (int i = 0; i < 64; ++i) {
        int o = 252 - i * 4;
        x[i] = ((uint32_t)b[o] << 24) | ((uint32_t)b[o+1] << 16) |
               ((uint32_t)b[o+2] << 8) | b[o+3];
    }
    return x;
}
void big_to_be(const Big& x, uint8_t* b) {
    for (int i = 0; i < 64; ++i) {
        int o = 252 - i * 4;
        b[o]   = (uint8_t)(x[i] >> 24); b[o+1] = (uint8_t)(x[i] >> 16);
        b[o+2] = (uint8_t)(x[i] >> 8);  b[o+3] = (uint8_t)x[i];
    }
}
// r = (a*b) mod N, a,b < N (Blakley interleaved modular multiply)
Big big_modmul(const Big& a, const Big& b, const Big& N) {
    Big r{};
    for (int k = 2047; k >= 0; --k) {
        big_shl1(r);
        if (big_cmp(r, N) >= 0) big_sub(r, N);
        if (big_bit(b, k)) {
            big_add(r, a);
            if (big_cmp(r, N) >= 0) big_sub(r, N);
        }
    }
    return r;
}
Big big_modexp(const Big& base, const Big& exp, const Big& N) {
    Big r{}; r[0] = 1;
    for (int k = 2047; k >= 0; --k) {
        r = big_modmul(r, r, N);
        if (big_bit(exp, k)) r = big_modmul(r, base, N);
    }
    return r;
}
Big big_r2(const Big& N) {              // 2^4096 mod N
    Big r{}; r[0] = 1;
    for (int i = 0; i < 4096; ++i) {
        big_shl1(r);
        if (big_cmp(r, N) >= 0) big_sub(r, N);
    }
    return r;
}

// ---- XeCryptRotSumSha -----------------------------------------------------
inline uint64_t be64(const uint8_t* p) {
    uint64_t v = 0; for (int i = 0; i < 8; ++i) v = (v << 8) | p[i]; return v;
}
inline void wbe64(uint8_t* p, uint64_t v) {
    for (int i = 7; i >= 0; --i) { p[i] = (uint8_t)v; v >>= 8; }
}
std::array<uint8_t, 20> rotsumsha(const uint8_t* data, int len) {
    uint64_t r[4] = {0, 0, 0, 0};
    for (int i = 0; i < len / 8; ++i) {
        uint64_t v = be64(data + i * 8);
        uint64_t s = r[1] + v;
        uint32_t u5 = (uint32_t)s, u6 = (uint32_t)(s >> 32);
        r[0] += (s < v) ? 1 : 0;
        r[1] = ((uint64_t)((u6 << 29) | (u5 >> 3)) << 32) | ((u6 >> 3) | (u5 << 29));
        uint64_t u1 = r[3] - v;
        uint32_t u4 = (uint32_t)(u1 >> 32), u1l = (uint32_t)u1;
        r[2] -= (v < u1) ? 1 : 0;
        r[3] = ((uint64_t)((u4 << 31) | (u1l >> 1)) << 32) | ((u4 >> 1) | (u1l << 31));
    }
    uint8_t rb[32], nb[32];
    for (int i = 0; i < 4; ++i) { wbe64(rb + i*8, r[i]); wbe64(nb + i*8, ~r[i]); }
    Sha1 s;
    s.update(rb, 32); s.update(rb, 32); s.update(data, len); s.update(nb, 32); s.update(nb, 32);
    return s.finish();
}

// ---- RC4 keystream (XeCrypt's PSS mask generator) -------------------------
void rc4(const uint8_t* key, int keylen, uint8_t* out, int n) {
    uint8_t S[256];
    for (int i = 0; i < 256; ++i) S[i] = (uint8_t)i;
    int j = 0;
    for (int i = 0; i < 256; ++i) {
        j = (j + S[i] + key[i % keylen]) & 0xff;
        uint8_t t = S[i]; S[i] = S[j]; S[j] = t;
    }
    int i = 0; j = 0;
    for (int k = 0; k < n; ++k) {
        i = (i + 1) & 0xff; j = (j + S[i]) & 0xff;
        uint8_t t = S[i]; S[i] = S[j]; S[j] = t;
        out[k] = S[(S[i] + S[j]) & 0xff];
    }
}

// ---- EMSA-PSS encode (RC4 mask, fixed salt), logical 256-byte block -------
void pss_encode(const uint8_t* msg_hash, const uint8_t* salt, uint8_t* out) {
    std::memset(out, 0, 256);
    out[0xe0] = 1;
    std::memcpy(out + 0xe1, salt, 10);
    Sha1 s;
    s.update(out, 8); s.update(msg_hash, 20); s.update(salt, 10);
    auto H = s.finish();
    std::memcpy(out + 0xeb, H.data(), 20);
    out[0xff] = 0xbc;
    uint8_t mask[0xeb];
    rc4(H.data(), 20, mask, 0xeb);
    for (int i = 0; i < 0xeb; ++i) out[i] ^= mask[i];
    out[0] &= 0x7f;
}

