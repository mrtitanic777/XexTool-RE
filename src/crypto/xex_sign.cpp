void devkit_sign(std::vector<uint8_t>& hdr, uint32_t data_offset, bool do_sign,
                 uint32_t sec_off) {
    // Every offset below is derived from the security-info offset; the constants
    // in the comments are the usual sec_off=0x120 case (17 optional headers).
    //   signature   @ sec+0x08          image-info  @ sec+0x108   (0x228)
    //   signed rgn    image-info[0:0x74]                          (0x228..0x29c)
    //   HeaderHash  @ image-info+0x5c                             (0x284)
    const uint32_t sig_off = sec_off + 8;          // 0x128
    const uint32_t img     = sec_off + 0x108;      // 0x228
    const uint32_t rgn_end = img + 0x74;           // 0x29c
    const uint32_t hh_off  = img + 0x5c;           // 0x284
    // 1. HeaderHash @0x284 = SHA1( hdr[0x29c:data_offset] || hdr[0:0x128] )
    Sha1 hs;
    hs.update(&hdr[rgn_end], data_offset - rgn_end);
    hs.update(&hdr[0], sig_off);
    auto hh = hs.finish();
    std::memcpy(&hdr[hh_off], hh.data(), 20);

    if (!do_sign) {                       // retail / no private key: zero the sig
        std::memset(&hdr[sig_off], 0, 256);
        return;
    }

    // 2. message digest over signed region hdr[0x228:0x29c] (image-info tail)
    auto mh = rotsumsha(&hdr[img], rgn_end - img);

    // 3. EMSA-PSS block
    static const uint8_t kSalt[10] = {'X','B','O','X','3','6','0','X','E','X'};
    uint8_t pss[256];
    pss_encode(mh.data(), kSalt, pss);

    // 4. sig = ((PSS * R^2) ^ d) mod N, stored qword-reversed
    Big N = big_from_be(kDevkitN), D = big_from_be(kDevkitD);
    Big P = big_from_be(pss);
    Big base = big_modmul(P, big_r2(N), N);
    Big sig = big_modexp(base, D, N);
    uint8_t sig_be[256];
    big_to_be(sig, sig_be);
    for (int i = 0; i < 256; i += 8)
        std::memcpy(&hdr[sig_off] + (256 - 8 - i), sig_be + i, 8); // qword-reverse
}

bool verify_devkit_sig(const uint8_t* sig256) {
    uint8_t q[256];
    for (int i = 0; i < 256; i += 8) std::memcpy(q + (256 - 8 - i), sig256 + i, 8);
    Big N = big_from_be(kDevkitN);
    Big s = big_from_be(q);
    if (big_cmp(s, N) >= 0) return false;
    Big base = big_modmul(big_modmul(s, s, N), s, N);          // s^3 mod N
    Big pss = big_modmul(base, big_from_be(kDevkitRinv2), N);  // * (R^2)^-1
    uint8_t pb[256];
    big_to_be(pss, pb);
    return pb[0xff] == 0xbc && (pb[0] & 0x80) == 0;
}

